class student
{
int id;
String name;
int marks;
student(int id,String name,int marks){
this.id=id;
this.name=name;
this.marks=marks;
}
public void setid(int sid)
{
this.id=sid;
}
public int getid()
{
return id;
}
public void setname(String sname)
{
this.name=sname;
}
public String getname()
{
return name;
}
public void setmarks(int smarks)
{
this.marks=smarks;
}
public int getmarks()
{
return marks;
}
}


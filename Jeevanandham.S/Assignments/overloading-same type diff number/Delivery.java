class Delivery
{
	
	public void delivery(String food)
	{
		System.out.println("Your "+food+" will be delivered in few minutes");
	}

	public void delivery(String food1, String food2)
	{
		System.out.println("Your "+food1+" and "+food2+" will be delivered in few minutes");
	}


	public void delivery(String food1, String food2, String food3)
	{
		System.out.println("Your "+food1+", "+food2+" and "+food3+" will be delivered in few minutes");
	}

	

}
public class EmailPojo {
    private String email;

    public EmailPojo(String email) {
        this.email = email;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return
                "Email=" + email ;
    }
}
